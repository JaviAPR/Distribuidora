const selectProductos = document.getElementById('productos');
const busquedaInput = document.getElementById('busqueda');
const opcCliente = document.getElementById('negoCliente');
const cantidad = document.getElementById('cantidad'); 
var factura = [];

    busquedaInput.addEventListener('input', function() {
        const busqueda = busquedaInput.value.toLowerCase();

        Array.from(selectProductos.querySelectorAll('option')).forEach(function(option) {
            const producto = option.textContent.toLowerCase();
            const optgroup = option.closest('optgroup');

            if (producto.includes(busqueda)) {
                option.style.display = 'block';
                optgroup.style.display = 'block';
            } else {
                option.style.display = 'none';

                const opcionesVisibles = Array.from(optgroup.querySelectorAll('option')).filter(opt => opt.style.display !== 'none');
                if (opcionesVisibles.length === 0) {
                    optgroup.style.display = 'none';
                }
            }
        });
    });

    function agregarItem(){
        
        var seleccion = selectProductos.value;
        var canti = cantidad.value;
        
        if(opcCliente.value === "negocio"){
            var precio = preciosProductosNegocio[seleccion];
        }else if(opcCliente.value === "cliente"){
            var precio = preciosProductosCliente[seleccion];
        }
        
        const total = cantidad.value * precio;

        

        factura.push({canti,seleccion,total});
        actualizarFactura();
    }

    function eliminarItem(){
        factura.pop();
        actualizarFactura();

    }

    function actualizarFactura() {
        const facturaList = document.getElementById("facturaList");
        facturaList.innerHTML = "";

        let totalF = 0;

        factura.forEach(item => {
            const listItem = document.createElement("li");
            listItem.textContent = `${item.canti} X ${item.seleccion} = $${item.total}`;
            facturaList.appendChild(listItem);
            totalF += item.total;
        });

        const totalItem = document.createElement("li");
        totalItem.textContent = `Total: $${totalF}`;
        facturaList.appendChild(totalItem);
    }